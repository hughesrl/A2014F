package com.relhs.asianfinder.data;

public class EmoticonsInfo {
	private int id;
    private String emoticonFile;
    private String emoticonText;


    private String date;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmoticonFile() {
        return emoticonFile;
    }

    public void setEmoticonFile(String emoticonFile) {
        this.emoticonFile = emoticonFile;
    }

    public String getEmoticonText() {
        return emoticonText;
    }

    public void setEmoticonText(String emoticonText) {
        this.emoticonText = emoticonText;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

}
